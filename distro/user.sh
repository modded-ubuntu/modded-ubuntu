create_user() {
    banner
    section_header "рҹ‘Ө CREATE YOUR UBUNTU USER ACCOUNT"
    
    echo ""
    echo -e "  ${CYAN_L}в•”в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•—${D}"
    echo -e "  ${CYAN_L}в•‘${Y}  Requirements:${D}                                                ${CYAN_L}в•‘${D}"
    echo -e "  ${CYAN_L}в•‘${W}    вҖў Username must be lowercase${D}                               ${CYAN_L}в•‘${D}"
    echo -e "  ${CYAN_L}в•‘${W}    вҖў No spaces or special characters${D}                          ${CYAN_L}в•‘${D}"
    echo -e "  ${CYAN_L}в•‘${W}    вҖў Password should be memorable${D}                             ${CYAN_L}в•‘${D}"
    echo -e "  ${CYAN_L}в•ҡв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•қ${D}"
    echo ""
    
    # Get username
    while true; do
        read -p "  $(echo -e ${GREEN_L}Enter username ${Y}[lowercase]${GREEN_L}: ${W})" user
        
        if [[ -z "$user" ]]; then
            error_msg "Username cannot be empty"
        elif [[ ! "$user" =~ ^[a-z][a-z0-9_-]*$ ]]; then
            error_msg "Username must start with lowercase letter and contain only a-z, 0-9, _, -"
        elif [[ ${#user} -lt 2 ]]; then
            error_msg "Username must be at least 2 characters"
        else
            break
        fi
    done
    echo ""
    
    # Get password
    while true; do
        read -sp "  $(echo -e ${GREEN_L}Enter password: ${W})" pass
        echo ""
        
        if [[ -z "$pass" ]]; then
            error_msg "Password cannot be empty"
        elif [[ ${#pass} -lt 4 ]]; then
            error_msg "Password must be at least 4 characters"
        else
            read -sp "  $(echo -e ${GREEN_L}Confirm password: ${W})" pass_confirm
            echo ""
            
            if [[ "$pass" != "$pass_confirm" ]]; then
                error_msg "Passwords do not match"
            else
                break
            fi
        fi
    done
    
    echo ""
    status_msg "Creating user account: ${Y}$user${D}"
    
    # Create user
    useradd -m -s $(which bash) "$user" 2>/dev/null
    usermod -aG sudo "$user"
